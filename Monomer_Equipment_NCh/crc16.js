function crc16_1(buf, len) {
	let index, i, ccc;
	let crch = 0xFF;  //初始化CRC高字节
	let crcl = 0xFF;  //初始化CRC低字节
	for (i = 0; i < len; i++){// 遍历输入缓冲区中的每个字节，执行CRC16校验码的计算
		ccc = buf[i];   // 获取当前字节的值
		index = crch ^ ccc;    // 计算高字节CRC并获取查表索引
		crch = crcl ^ crc_high[index];    // 更新高字节CRC值
		crcl = crc_low[index];  // 更新低字节CRC值
	}
	return ((crch << 8) | crcl);// 返回计算得到的CRC16校验码，将高字节左移8位并与低字节进行按位或操作，以组合成完整的16位校验码
}
function crc16_2(buf, len){
	let crc = 0xffff; // 初始化CRC值为0xFFFF
	let ccc, i, j; // 定义临时变量ccc用于存储当前处理的字节，以及循环变量i和j
	for (i = 0; i < len; i++) // 遍历缓冲区中的每个字节
	{
		ccc = buf[i];  // 将当前字节的值存储到ccc中
		crc ^= ccc; // 将当前字节的值与CRC值进行异或操作
		for (j = 8; j != 0; j--)   // 对当前CRC值进行位操作，循环8次，对应一个字节的8位
		{
			if ((crc & 0x0001) != 0)  // 检查CRC值的最低位是否为1 
			{
				crc >>= 1; // 如果最低位为1，将CRC值右移一位，然后与固定值0xA001进行异或操作
				crc ^= 0xA001;
			}
			else
			{  // 如果最低位不为1，仅将CRC值右移一位
				crc >>= 1; 
			}
		}
	}
	return crc;   // 返回最终计算得到的CRC16校验值
}